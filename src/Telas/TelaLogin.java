public void entrar(){
     String login = TextoLogin.getText().trim();
        String senha = new String(TextoSenha.getPassword());

        if (login.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.");
            return;
        }

        LoginD loginD = new LoginD();
        String cargo = loginD.login(FuncionarioDao.getFuncionarios(), login, senha);

        if (cargo != null) {
            cargo = cargo.toLowerCase();

            if (cargo.equals("total")) {
                JOptionPane.showMessageDialog(null, " Bem-vindo, " + login + "  Bom trabalho ");
                TelaInicial t = new TelaInicial(cargo);
                t.setVisible(true);
            } else {
                TelaInicial t = new TelaInicial(cargo);

                t.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, " Usu√°rio ou senha incorretos!");
        }  
}
